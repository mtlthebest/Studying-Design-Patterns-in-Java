    long passedTime;
    long startTime;
    
    SimpleDateFormat displayFormat =
            new SimpleDateFormat("HH:mm:ss.SSS");

    private abstract class State{
        public abstract void stopStart();
        public abstract void reset();
    }
    
    private class StandBy extends State{
        
        @Override
        public void stopStart() {
            currentState = measuring;
            startTime = new Date().getTime();
            display.setText("Measuring...");
        }

        @Override
        public void reset() { 
            //Nothing to do.
        }
        
    }
    
    private class Measuring extends State{

        @Override
        public void stopStart() {
            passedTime += new Date().getTime() - startTime;
            display.setText(
                    displayFormat.format(passedTime)
                    );
            currentState = stopping;
        }

        @Override
        public void reset() {
            //Nothing to do.
        }
        
    }
    
    private class Stopping extends State{

        @Override
        public void stopStart() {
            currentState = measuring;
            startTime = new Date().getTime();
            display.setText("Measuring...");
        }

        @Override
        public void reset() {
            passedTime = 0;            
            display.setText(
                displayFormat.format(
                    new Date(passedTime)
                )
        );
        }
        
    }
    
    
