    public StopWatch() {
        initComponents();
        this.standBy = new StandBy();
        this.measuring = new Measuring();
        this.stopping = new Stopping();
        this.currentState = this.standBy;
        this.displayFormat.setTimeZone(TimeZone.getTimeZone("GMT"));
        this.display.setText(
                displayFormat.format(
                    new Date(this.passedTime)
                )
        );
    }

